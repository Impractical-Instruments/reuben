    #[test]
    fn differentiate_dt_carries_across_blocks() {
        // Event at block-1 frame 0, next at block-2 frame 0 (256 samples later).
        let mut d = Differentiate::new();
        let _ = run_msg(&mut d, &[], &[val("in", 0.0, 0)]);
        let emits = run_msg(&mut d, &[], &[val("in", 1.0, 0)]);
        approx::assert_relative_eq!(
            emits[0].args[0].as_f32().unwrap(),
            1.0 / (256.0 / SR),
            epsilon = 1e-1
        );
    }

    #[test]
    fn integrate_accumulates_value_times_dt() {
        // Hold value 2.0 from frame 0 to frame 100, then read at 100: acc = 2.0 * 100/48000.
        let mut i = Integrate::new();
        let emits = run_msg(&mut i, &[], &[val("in", 2.0, 0), val("in", 9.0, 100)]);
        approx::assert_relative_eq!(emits[0].args[0].as_f32().unwrap(), 0.0); // seed
        approx::assert_relative_eq!(
            emits[1].args[0].as_f32().unwrap(),
            2.0 * (100.0 / SR),
            epsilon = 1e-6
        );
    }

    #[test]
    fn spawned_math_ops_start_fresh() {
        let mut d = Differentiate::new();
        let _ = run_msg(&mut d, &[], &[val("in", 0.0, 0), val("in", 5.0, 50)]);
        let mut d2 = d.spawn();
        // Fresh: first event after spawn seeds (no emission), exactly like a new op.
        let emits = run_msg(&mut *d2, &[], &[val("in", 1.0, 0)]);
        assert!(emits.is_empty(), "spawned differentiate must re-seed");
    }
