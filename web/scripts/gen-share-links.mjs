#!/usr/bin/env node
// README share-link generator + checker for the reuben web player (issue #228, P6).
//
// The share codec (crates/reuben-web/js/share.mjs) turns a whole playable instrument BUNDLE —
// the document plus every transitive TEXT resource the engine's discovery pass collected — into
// a `#r1.…` URL fragment. This script mints one such link per sample-free example rig and writes
// it into the README's "Rig" table as a ▶ play link, so a reader can open any pristine-default
// rig in the hosted player with one click.
//
// TWO MODES:
//   (write, default)  node scripts/gen-share-links.mjs
//       Instantiate the release wasm, run the engine's OWN fetch-on-miss discovery over each rig
//       (the SAME loop stage-assets.mjs runs), mint a link for every sample-free rig, and rewrite
//       README.md in place — a ▶ play link per row + a generated reference-definitions block.
//   (check)           node scripts/gen-share-links.mjs --check
//       Extract every `#r1.…` from README.md, decodeBundle each, and assert the decoded docText
//       equals instruments/<id>.json on disk (and the embedded surface equals the resolved
//       surfaces/<id> doc, presence included). DECODE-only (deflate output is not byte-stable
//       across zlib versions, so we never re-encode-and-compare) — this catches a rig document
//       edited after its link was minted, which would otherwise silently play the OLD bytes.
//       Exits non-zero on any mismatch or a sample-free rig missing its link. Doubles as an
//       end-to-end exercise of the codec against every real rig document.
//
// PREDICATE — "every sample-free instrument in the README rig table". A rig is EXCLUDED iff its
// discovery reports any resource with kind===1 (a WAV sample). This is the identical discovery
// stage-assets.mjs performs and the identical kind that share.mjs's encodeBundle refuses at mint
// (KIND_SAMPLE=1, the WAV trust boundary, ADR-0042 decision 3): generator and codec share one
// predicate and cannot drift. A wav-pulling rig would be excluded (none is in the table today);
// mic-space takes live mic input (an input pipe, not a resource) → sample-free → included.

import { access, readFile, writeFile } from "node:fs/promises";
import { constants } from "node:fs";
import { dirname, join, resolve } from "node:path";
import { fileURLToPath } from "node:url";

import { loadInstrument } from "../../crates/reuben-web/js/loader.mjs";
import { encodeBundle, decodeBundle, CAPS } from "../../crates/reuben-web/js/share.mjs";

const HERE = dirname(fileURLToPath(import.meta.url)); // web/scripts
const WEB = resolve(HERE, ".."); // web
const ROOT = resolve(WEB, ".."); // repo root
const CRATE = join(ROOT, "crates", "reuben-web");
const WASM = join(CRATE, "target", "wasm32-unknown-unknown", "release", "reuben_web.wasm");
const INSTRUMENTS = join(ROOT, "instruments");
const SURFACES = join(ROOT, "surfaces");
const README = join(ROOT, "README.md");

const SAMPLE_RATE = 48000; // any rate enumerates the same resource set; construct() only needs one

// The canonical PRODUCTION origin the committed links point at. The Cloudflare Pages project is
// `reuben-web-player` (see the deploy-web job in .github/workflows/ci.yml; no custom domain is
// configured), so its default hostname is the origin below.
//
// Changing this invalidates every committed README link; it is NOT an env var because the output
// is committed and must not depend on who ran the script.
const ORIGIN = "https://reuben-web-player.pages.dev";

// Stable markers around the generated reference-definitions block, so a re-run regenerates only
// that block and running twice produces no further diff (idempotent).
const BEGIN_MARK = "<!-- BEGIN share-links (generated by web/scripts/gen-share-links.mjs) -->";
const END_MARK = "<!-- END share-links -->";

// The ▶ play link appended to a rig row, and a regex that matches it for stripping (idempotency).
const playLinkText = (id) => ` [▶ play][${id}-link]`;
const PLAY_LINK_RE = / \[▶ play\]\[[a-z0-9-]+-link\]/g;

async function exists(p) {
  try {
    await access(p, constants.R_OK);
    return true;
  } catch {
    return false;
  }
}

// --- wasm instantiate (the stage-assets.mjs idiom) ---------------------------------------

async function instantiate() {
  if (!(await exists(WASM))) {
    throw new Error(
      `wasm artifact missing: ${WASM}\n` +
        "  build it first: (cd crates/reuben-web && cargo build --release --target wasm32-unknown-unknown)",
    );
  }
  const bytes = await readFile(WASM);
  let mem = null;
  const { instance } = await WebAssembly.instantiate(bytes, {
    env: {
      log: (ptr, len) => {
        if (!mem) return;
        console.log(`  [wasm] ${new TextDecoder().decode(new Uint8Array(mem.buffer, ptr, len))}`);
      },
    },
  });
  const ex = instance.exports;
  mem = ex.memory;
  if (typeof ex._initialize === "function") ex._initialize();
  else if (typeof ex.__wasm_call_ctors === "function") ex.__wasm_call_ctors();
  return ex;
}

// --- surface resolution ------------------------------------------------------------------

// The ADR-0043 §5 resolution order for the share target, read from disk: the web override
// first, then the base doc, else null (the receiver auto-derives). Unlike the player's
// dark-degrade, a surface file that exists but doesn't parse or declares an unknown
// surface_version THROWS — the generator must never commit a link carrying a broken surface.
async function resolveSurfaceText(id) {
  for (const candidate of [join(SURFACES, `${id}.web.json`), join(SURFACES, `${id}.json`)]) {
    if (!(await exists(candidate))) continue;
    const text = await readFile(candidate, "utf8");
    const doc = JSON.parse(text); // a syntax error throws with the JSON.parse message
    if (doc?.surface_version !== 1) {
      throw new Error(`${candidate} declares surface_version ${doc?.surface_version}; expected 1`);
    }
    return text;
  }
  return null;
}

// --- rig table parsing -------------------------------------------------------------------

// Pull the rig ids from the README rig table: rows whose FIRST cell is a single backtick-wrapped
// identifier (`| `default` | … |`). The skills table further down has prose first cells and does
// not match, so this selects exactly the rig rows.
function parseRigIds(readmeText) {
  const ids = [];
  for (const line of readmeText.split("\n")) {
    const m = line.match(/^\|\s*`([a-z0-9-]+)`\s*\|/);
    if (m) ids.push(m[1]);
  }
  return ids;
}

// --- discovery: sample-free classification + bundle collection ---------------------------

// For each rig id (in README table order) whose document exists on disk, replay the engine's
// fetch-on-miss discovery reading resources from instruments/. Classify:
//   - any kind===1 miss  ⇒ EXCLUDED, reason "sample" — sample-bearing (the WAV trust boundary);
//   - construct throws   ⇒ EXCLUDED, reason "unbuildable" — the web engine can't run it (e.g.
//                          stereo-sub wants 3 output channels; the stereo worklet carries 2), so
//                          a minted link could never boot in the player anyway;
//   - otherwise          ⇒ LINKED, collecting docText + every transitive TEXT resource.
// Returns { linked: [{id, docText, resources}], excluded: [{id, reason, detail}] }.
async function discoverRigs(ex, ids) {
  const linked = [];
  const excluded = [];
  for (const id of ids) {
    const docPath = join(INSTRUMENTS, `${id}.json`);
    if (!(await exists(docPath))) continue; // a rig row with no document on disk — skip silently
    const docText = await readFile(docPath, "utf8");
    const resources = [];
    let sample = null;
    try {
      await loadInstrument(ex, docText, SAMPLE_RATE, async (key, kind) => {
        const bytes = new Uint8Array(await readFile(join(INSTRUMENTS, key)));
        if (kind === 1) {
          // A WAV sample — the rig is sample-bearing. The first one is enough to exclude; still
          // return the bytes so discovery can finish cleanly.
          if (!sample) sample = key;
        } else {
          resources.push({ key, kind: 0, bytes });
        }
        return bytes;
      });
    } catch (e) {
      ex.destroy(); // reset the engine before the next rig, even on failure
      excluded.push({ id, reason: "unbuildable", detail: e.message });
      continue;
    }
    ex.destroy(); // rig-switch lifecycle: drop this engine, keep the instance for the next rig
    if (sample) excluded.push({ id, reason: "sample", detail: sample });
    else linked.push({ id, docText, resources });
  }
  return { linked, excluded };
}

// --- README rewrite ----------------------------------------------------------------------

function stripGeneratedBlock(text) {
  const b = text.indexOf(BEGIN_MARK);
  const e = text.indexOf(END_MARK);
  if (b === -1 || e === -1) return text.replace(/\s+$/, "") + "\n";
  const before = text.slice(0, b).replace(/\s+$/, "");
  const after = text.slice(e + END_MARK.length).replace(/^\s+/, "");
  return `${before}\n${after ? after.replace(/\s+$/, "") + "\n" : ""}`;
}

// Append ` [▶ play][<id>-link]` to the "What it is" cell of each linked rig row, after first
// stripping any existing play link from every rig row (idempotency). The play link goes just
// inside the row's trailing `|`.
function addPlayLinks(text, linkedIds) {
  const linked = new Set(linkedIds);
  return text
    .split("\n")
    .map((line) => {
      const m = line.match(/^\|\s*`([a-z0-9-]+)`\s*\|/);
      if (!m) return line;
      const id = m[1];
      let row = line.replace(/\s*$/, ""); // right-trim
      if (!row.endsWith("|")) return line; // not a well-formed table row
      row = row.slice(0, -1).replace(/\s*$/, ""); // drop trailing `|` and its padding
      row = row.replace(PLAY_LINK_RE, ""); // strip any prior play link(s)
      if (linked.has(id)) row += playLinkText(id);
      return `${row} |`;
    })
    .join("\n");
}

function buildDefinitionsBlock(links) {
  const lines = [
    BEGIN_MARK,
    "<!-- Play links for the sample-free rigs above: a full playable bundle (document + every",
    "     transitive text resource) encoded into the URL fragment by web/scripts/gen-share-links.mjs.",
    "     Run `node web/scripts/gen-share-links.mjs` to regenerate; `--check` verifies them. -->",
    ...links.map(({ id, url }) => `[${id}-link]: ${url}`),
    END_MARK,
  ];
  return lines.join("\n");
}

async function writeMode() {
  const ex = await instantiate();
  const readme = await readFile(README, "utf8");
  const ids = parseRigIds(readme);
  const { linked, excluded } = await discoverRigs(ex, ids);

  // Mint a link per sample-free rig (NO snapshot — README links are pristine defaults; the
  // curated surface travels when one exists, so the link renders the same UI as the launcher).
  const links = [];
  for (const { id, docText, resources } of linked) {
    const surfaceText = await resolveSurfaceText(id);
    const fragment = await encodeBundle({ docText, resources, surfaceText });
    const url = `${ORIGIN}/#${fragment}`;
    links.push({ id, url, fragment, resourceCount: resources.length, hasSurface: surfaceText != null });
  }

  // Rewrite: strip old generated block, refresh play links in the table, append the new block.
  let out = stripGeneratedBlock(readme);
  out = addPlayLinks(out, linked.map((l) => l.id));
  out = `${out.replace(/\s+$/, "")}\n\n${buildDefinitionsBlock(links)}\n`;
  await writeFile(README, out);

  // Report.
  console.log(`[gen-share-links] origin: ${ORIGIN}`);
  console.log(`[gen-share-links] linked ${links.length} sample-free rig(s):`);
  for (const { id, fragment, resourceCount, hasSurface } of links) {
    console.log(
      `  ${id.padEnd(18)} ${String(fragment.length).padStart(5)} B  ` +
        `(${resourceCount} text resource${resourceCount === 1 ? "" : "s"}` +
        `${hasSurface ? ", curated surface" : ""})`,
    );
  }
  console.log(`[gen-share-links] excluded ${excluded.length} rig(s):`);
  for (const { id, reason, detail } of excluded) {
    const why =
      reason === "sample"
        ? `references ${detail} (kind=1 WAV sample)`
        : `unbuildable in the web engine (${detail})`;
    console.log(`  ${id.padEnd(18)} — ${why}`);
  }
  const largest = links.reduce((a, b) => (b.fragment.length > a.fragment.length ? b : a), links[0]);
  if (largest) {
    console.log(
      `[gen-share-links] largest fragment: ${largest.id} at ${largest.fragment.length} B ` +
        `(cap ${CAPS.FRAGMENT_BYTES} B)`,
    );
  }
  console.log("[gen-share-links] README.md rewritten.");
}

// --- check mode --------------------------------------------------------------------------

// Parse the generated reference definitions `[<id>-link]: <url>` back into id → fragment.
function parseLinkDefs(readmeText) {
  const defs = new Map();
  const re = /^\[([a-z0-9-]+)-link\]:\s+(\S+)\s*$/gm;
  let m;
  while ((m = re.exec(readmeText)) !== null) {
    const id = m[1];
    const url = m[2];
    const hash = url.indexOf("#");
    if (hash === -1) continue;
    defs.set(id, url.slice(hash + 1)); // fragment, without the leading `#`
  }
  return defs;
}

// DECODE-only, by design (issue #228): decode is deterministic even though deflate ENCODE is
// not stable across zlib versions, so this compares each committed link's decoded document to
// the file on disk WITHOUT running the engine. It needs only Node + share.mjs — no wasm, no
// discovery — which is why CI can run it before (and independently of) the app build. A
// regenerate-and-diff would instead break the day a zlib upgrade rewrote every encoding.
async function checkMode() {
  const readme = await readFile(README, "utf8");
  const defs = parseLinkDefs(readme); // id → fragment, from the generated definitions block

  const errors = [];

  // 1. Text consistency: every `[▶ play][<id>-link]` reference in the table resolves to a
  // definition, and every definition is referenced — no dangling or orphaned links. Pure text,
  // no engine needed.
  const referenced = new Set();
  const refRe = /\[▶ play\]\[([a-z0-9-]+)-link\]/g;
  let rm;
  while ((rm = refRe.exec(readme)) !== null) {
    referenced.add(rm[1]);
    if (!defs.has(rm[1])) errors.push(`play-link reference [${rm[1]}-link] has no definition`);
  }
  for (const id of defs.keys()) {
    if (!referenced.has(id)) errors.push(`link definition [${id}-link] is never referenced`);
  }

  // 2. Decode each link and compare its docText bytes to instruments/<id>.json on disk — the
  // staleness gate: an edited rig whose link wasn't regenerated fails here.
  const encoder = new TextEncoder();
  let checked = 0;
  for (const [id, fragment] of defs) {
    const docPath = join(INSTRUMENTS, `${id}.json`);
    if (!(await exists(docPath))) {
      errors.push(`link \`${id}\` maps to no instruments/${id}.json on disk`);
      continue;
    }
    const onDisk = encoder.encode(await readFile(docPath, "utf8"));
    let decoded;
    try {
      decoded = await decodeBundle(fragment);
    } catch (e) {
      errors.push(`link \`${id}\` failed to decode: ${e.code ?? "?"} — ${e.message}`);
      continue;
    }
    const linkBytes = encoder.encode(decoded.docText);
    const same =
      linkBytes.length === onDisk.length && linkBytes.every((b, i) => b === onDisk[i]);
    if (!same) {
      errors.push(
        `link \`${id}\` docText (${linkBytes.length} B) != instruments/${id}.json ` +
          `(${onDisk.length} B) — the rig was edited after its link was minted; regenerate.`,
      );
    }

    // 3. Surface staleness, same shape as the docText gate: the link's embedded surface must
    // byte-match the on-disk resolved surface — including PRESENCE in both directions, so a
    // newly added (or deleted) surface doc without a regenerated link fails here.
    let onDiskSurface;
    try {
      onDiskSurface = await resolveSurfaceText(id);
    } catch (e) {
      errors.push(`surface doc for \`${id}\` is unusable: ${e.message}`);
      continue;
    }
    if (decoded.surfaceText == null && onDiskSurface != null) {
      errors.push(
        `link \`${id}\` carries no surface but surfaces/ resolves one on disk — regenerate.`,
      );
    } else if (decoded.surfaceText != null && onDiskSurface == null) {
      errors.push(
        `link \`${id}\` carries a surface but surfaces/ resolves none on disk — regenerate.`,
      );
    } else if (decoded.surfaceText !== onDiskSurface) {
      errors.push(
        `link \`${id}\` surface != the on-disk resolved surface doc — ` +
          `the surface was edited after its link was minted; regenerate.`,
      );
    }
    checked++;
  }

  if (errors.length > 0) {
    console.error(`[gen-share-links --check] FAILED (${errors.length} problem(s)):`);
    for (const e of errors) console.error(`  - ${e}`);
    process.exit(1);
  }
  console.log(
    `[gen-share-links --check] OK — ${checked} link(s) decode to their on-disk rig documents and surfaces.`,
  );
}

// --- entry -------------------------------------------------------------------------------

const isCheck = process.argv.includes("--check");
(isCheck ? checkMode() : writeMode()).catch((err) => {
  console.error(`[gen-share-links] FAILED: ${err.message}`);
  process.exit(1);
});
