#!/usr/bin/env python3
"""Regenerate `reuben_eval/tokenizer/unicode_classes.py` — the frozen `\\p{L}`/`\\p{N}` tables.

Running this is a **deliberate, reviewable act with a visible baseline reset**, which is the whole
point. see rules: agent-mcp

The gate's tokenizer must produce the same token count for the same bytes across years, or the
`eval-history.jsonl` trend silently re-baselines and cross-commit comparison becomes a lie. The
cl100k_base pre-tokenizer needs `\\p{L}` and `\\p{N}`, which stdlib `re` does not implement — so we
materialise them as explicit codepoint ranges. Building those ranges from *live* `unicodedata` at
gate time would tie the trend to whatever Unicode version the CI runner's CPython happens to bundle:
a routine Python upgrade would move the character classes, re-tokenize every payload, and reset the
series with nothing in the diff to show for it.

So the ranges are generated here, once, and committed as data with a sha256 pin
(`reuben_eval/tokenizer/pins.json`). `cl100k.py` refuses to count a token until both that file and
the vendored vocab match their pins.

    python3 eval/tools/gen_unicode_classes.py        # rewrite the table
    python3 eval/tools/gen_unicode_classes.py --check # assert it is up to date with THIS interpreter

`--check` is not run in CI on purpose: a Python upgrade legitimately changes the answer, and the
committed table is authoritative regardless. It exists so a human regenerating on purpose can see
what moved.

Full findings behind the pin: `docs/research/harness-rungs.md` §6.
"""

from __future__ import annotations

import argparse
import sys
import unicodedata
from pathlib import Path

# `\p{L}` and `\p{N}` in the cl100k_base pattern, spelled as the general categories they abbreviate.
LETTER_CATEGORIES = frozenset({"Lu", "Ll", "Lt", "Lm", "Lo"})
NUMBER_CATEGORIES = frozenset({"Nd", "Nl", "No"})

MAX_CODEPOINT = 0x110000
OUT = Path(__file__).resolve().parent.parent / "reuben_eval" / "tokenizer" / "unicode_classes.py"


def ranges_for(categories: frozenset[str]) -> list[tuple[int, int]]:
    """Contiguous [lo, hi] codepoint ranges whose general category is in `categories`.

    Surrogates (U+D800..U+DFFF) can't appear in a `str` CPython will hand us, and
    `unicodedata.category` reports them as `Cs` anyway, so they fall out naturally.
    """
    ranges: list[tuple[int, int]] = []
    start: int | None = None
    for cp in range(MAX_CODEPOINT):
        if unicodedata.category(chr(cp)) in categories:
            if start is None:
                start = cp
        elif start is not None:
            ranges.append((start, cp - 1))
            start = None
    if start is not None:
        ranges.append((start, MAX_CODEPOINT - 1))
    return ranges


def escape(cp: int) -> str:
    """A codepoint as an unambiguous escape for a `re` character-class body.

    Everything is escaped numerically rather than emitted literally: inside a class, `\\`, `]`, `^`
    and `-` are metacharacters, and a table full of raw exotic glyphs is unreviewable in a diff.
    """
    return f"\\u{cp:04x}" if cp <= 0xFFFF else f"\\U{cp:08x}"


def class_body(ranges: list[tuple[int, int]]) -> str:
    """Render ranges as the inside of a `[...]` character class."""
    return "".join(escape(lo) if lo == hi else f"{escape(lo)}-{escape(hi)}" for lo, hi in ranges)


def render() -> str:
    letters = ranges_for(LETTER_CATEGORIES)
    numbers = ranges_for(NUMBER_CATEGORIES)
    return f'''"""GENERATED by `eval/tools/gen_unicode_classes.py` — do not edit by hand.

Frozen `\\\\p{{L}}` / `\\\\p{{N}}` codepoint ranges for the cl100k_base pre-tokenizer, so the gate's
token counts never move when the CI runner's CPython bundles a new Unicode version. Hash-pinned in
`pins.json`; regenerating is a deliberate baseline reset. see rules: agent-mcp

Unicode {unicodedata.unidata_version} · {len(letters)} letter ranges · {len(numbers)} number ranges
"""

UNICODE_VERSION = "{unicodedata.unidata_version}"

# Character-class bodies: the text between `[` and `]`, ready to interpolate into a pattern.
LETTER = (
    "{class_body(letters)}"
)

NUMBER = (
    "{class_body(numbers)}"
)
'''


def main() -> int:
    parser = argparse.ArgumentParser(description=__doc__)
    parser.add_argument(
        "--check",
        action="store_true",
        help="exit non-zero if the committed table differs from what this interpreter generates",
    )
    args = parser.parse_args()

    generated = render()
    if args.check:
        current = OUT.read_text(encoding="utf-8") if OUT.exists() else ""
        if current != generated:
            print(
                f"{OUT} differs from this interpreter's Unicode "
                f"{unicodedata.unidata_version} tables.\n"
                "That is expected across CPython upgrades and does NOT need fixing: the committed "
                "table is the pin. Regenerate only when you intend to reset the eval trend.",
                file=sys.stderr,
            )
            return 1
        print(f"{OUT} is up to date (Unicode {unicodedata.unidata_version}).")
        return 0

    OUT.write_text(generated, encoding="utf-8")
    print(f"Wrote {OUT} (Unicode {unicodedata.unidata_version}, {len(generated)} chars).")
    print("Remember to refresh the sha256 in reuben_eval/tokenizer/pins.json.")
    return 0


if __name__ == "__main__":
    raise SystemExit(main())
